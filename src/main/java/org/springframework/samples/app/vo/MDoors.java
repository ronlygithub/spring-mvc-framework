package org.springframework.samples.app.vo;

public class MDoors {
	private String id;
	private String house_id;
	private String project_id;
	private String addr;
	private String doorplate;
	private Float living_area;
	private Float public_area;
	private Float balcony_area;
	private Float selling_area;
	private Float building_area;
	private String type;
	private String status;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getHouse_id() {
		return house_id;
	}
	public void setHouse_id(String house_id) {
		this.house_id = house_id;
	}
	public String getProject_id() {
		return project_id;
	}
	public void setProject_id(String project_id) {
		this.project_id = project_id;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getDoorplate() {
		return doorplate;
	}
	public void setDoorplate(String doorplate) {
		this.doorplate = doorplate;
	}
	public float getLiving_area() {
		return living_area;
	}
	public void setLiving_area(float living_area) {
		this.living_area = living_area;
	}
	public float getPublic_area() {
		return public_area;
	}
	public void setPublic_area(float public_area) {
		this.public_area = public_area;
	}
	public float getBalcony_area() {
		return balcony_area;
	}
	public void setBalcony_area(float balcony_area) {
		this.balcony_area = balcony_area;
	}
	public float getSelling_area() {
		return selling_area;
	}
	public void setSelling_area(float selling_area) {
		this.selling_area = selling_area;
	}
	public float getBuilding_area() {
		return building_area;
	}
	public void setBuilding_area(float building_area) {
		this.building_area = building_area;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}
